#define MEM_SIZE (512 * 1024 * 1024)
#define FB_OFFSET (MEM_SIZE - 800 * 600 * 3) // not needed!!

typedef struct { // Not needed
        uint8_t r;
        uint8_t g;
        uint8_t b;
} pixel;

pixel pix(uint8_t r, uint8_t g, uint8_t b) { // not needed
        pixel p;
        p.r = r;
        p.g = g;
        p.b = b;
        return p;
}

void draw(uint16_t x, uint16_t y, pixel p, uint8_t* mem) {
        // u dont need this
        unsigned int addr = FB_OFFSET + (y * 800 + x) * 3;
        mem[addr] = p.r;
        mem[addr+1] = p.g;
        mem[addr+2] = p.b;
}


