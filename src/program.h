void drawchar(uint16_t x, uint16_t y, char c, pixel col, uint8_t *mem) {
	if ((unsigned char)c > 127) c = '?';
	const uint8_t *glyph = font8x8[(unsigned char)c];
	for (int row = 0; row < 8; row++) {
		for (int col2 = 0; col2 < 8; col2++) {
			if (glyph[row] & (1 << col2))
				draw(x + col2, y + row, col, mem);
		}
	}
}

void drawtext(uint16_t x, uint16_t y, const char *s, pixel col, uint8_t *mem) {
	for (int i = 0; s[i]; i++)
		drawchar(x + i * 8, y, s[i], col, mem);
}


uint16_t x;
uint16_t y;
uint8_t  r;
uint8_t  g;
uint8_t  b;

void init(uint8_t* mem) {
	x = 0;
	y = 0;
	r = 0;
	g = 0;
	b = 0;
}

void update(uint8_t* mem) {
	for (int i = 0; i <  480000; i++) {
		draw(x, y, pix(r, g, b), mem);
		x++;
		if (x >= 800) {
			x = 0;
			y++;
			if (y >= 600) {
				y = 0;
			}
		}
		r++;
		if (r == 255) {
			r = 0;
			g++;
			if (g == 255) {
				g = 0;
				b++;
			}
		}
	}
	drawtext((800 - 72) / 2, (600 - 8) / 2, "BSCR Demo", pix(0xFF, 0xFF, 0xFF), mem);
	usleep(16000);
}
